int	ft_isalnum(char c)
{
	int		is_number;
	int		is_alpha;

	is_number = (c >= '0' && c <= '9');
	is_alpha = ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'));
	return (is_number || is_alpha);
}
